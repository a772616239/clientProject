package model.drawCard;

public class DrawPetRateBean {
    private int petId;
    private int petcore;
    private int suipianId;
    private int num;
    private int unlocklimit;

    public int getPetId() {
        return petId;
    }

    public void setPetId(int petId) {
        this.petId = petId;
    }

    public int getSuipianId() {
        return suipianId;
    }

    public void setSuipianId(int suipianId) {
        this.suipianId = suipianId;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getUnlocklimit() {
        return unlocklimit;
    }

    public void setUnlocklimit(int unlocklimit) {
        this.unlocklimit = unlocklimit;
    }

    public int getPetcore() {
        return petcore;
    }

    public void setPetcore(int petcore) {
        this.petcore = petcore;
    }
}
